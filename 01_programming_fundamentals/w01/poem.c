#include <stdio.h>

int main(void)
{
    printf("Together were two cages hung,\n");
    printf("For music and for show;\n");
    printf("In one a fine canary sung,\n");
    printf("In t'other screech'd a crow.\n");
    printf("One charm'd the household with his song,\n");
    printf("The other vex'd it with his cries;\n");
    printf("Forever cawing all day long,\n");
    printf("He call'd for bread, and cakes, and pies.\n");
    return 0;
}